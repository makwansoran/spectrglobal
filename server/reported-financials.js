/**
 * Parse Finnhub /stock/financials-reported (SEC filings) into Spectr financial shapes.
 */
const finnhub = require("./finnhub");

const CACHE_MS = 3600_000;

function findLine(lines, matchers) {
  if (!Array.isArray(lines)) return null;
  for (const m of matchers) {
    const row = lines.find((x) => {
      const concept = String(x.concept || "").toLowerCase();
      const label = String(x.label || "").toLowerCase();
      if (m.concept && concept.includes(String(m.concept).toLowerCase())) return true;
      if (m.labelRe && m.labelRe.test(label)) return true;
      if (m.label && label.includes(String(m.label).toLowerCase())) return true;
      return false;
    });
    if (row?.value != null && !Number.isNaN(Number(row.value))) return Number(row.value);
  }
  return null;
}

function parseIncomeStatement(ic) {
  return {
    revenue: findLine(ic, [
      { concept: "RevenueFromContractWithCustomer" },
      { concept: "Revenues" },
      { label: "net sales" },
      { labelRe: /^total revenue/ },
      { label: "revenue" },
    ]),
    grossProfit: findLine(ic, [{ concept: "GrossProfit" }, { label: "gross profit" }, { label: "gross margin" }]),
    operatingIncome: findLine(ic, [
      { concept: "OperatingIncomeLoss" },
      { label: "operating income" },
      { label: "operating profit" },
    ]),
    netIncome: findLine(ic, [
      { concept: "NetIncomeLoss" },
      { labelRe: /^net income(?!\s*\()/ },
      { label: "net income attributable" },
      { label: "profit for the period" },
    ]),
  };
}

function parseBalanceSheet(bs) {
  return {
    totalAssets: findLine(bs, [
      { concept: "Assets" },
      { label: "total assets" },
    ]),
    totalLiabilities: findLine(bs, [
      { concept: "Liabilities" },
      { label: "total liabilities" },
    ]),
  };
}

function parseCashFlow(cf) {
  return {
    operatingCashFlow: findLine(cf, [
      { concept: "NetCashProvidedByUsedInOperatingActivities" },
      { label: "cash generated by operating" },
      { label: "net cash provided by operating" },
    ]),
  };
}

function periodKey(year, quarter) {
  return quarter ? `${year}-Q${quarter}` : `${year}`;
}

function normalizeReportRow(row, freq) {
  const ic = row.report?.ic || [];
  const bs = row.report?.bs || [];
  const cf = row.report?.cf || [];
  const income = parseIncomeStatement(ic);
  const balance = parseBalanceSheet(bs);
  const cash = parseCashFlow(cf);

  const year = Number(row.year);
  const quarter = freq === "quarterly" ? Number(row.quarter) : null;
  const periodEnd = String(row.endDate || row.filedDate || "").slice(0, 10);

  return {
    period: periodKey(year, quarter),
    fiscalYear: year,
    fiscalQuarter: quarter || undefined,
    periodEnd,
    form: row.form || null,
    filedDate: String(row.filedDate || "").slice(0, 10) || null,
    ...income,
    ...balance,
    operatingCashFlow: cash.operatingCashFlow,
    ebitda: null,
  };
}

function withinYears(row, yearsBack = 5) {
  const end = row.periodEnd ? new Date(row.periodEnd) : null;
  if (!end || Number.isNaN(end.getTime())) return true;
  const cutoff = new Date();
  cutoff.setFullYear(cutoff.getFullYear() - yearsBack);
  return end >= cutoff;
}

function dedupePeriods(rows) {
  const map = new Map();
  for (const row of rows) {
    const key = row.period;
    const existing = map.get(key);
    if (!existing || String(row.filedDate || "") > String(existing.filedDate || "")) {
      map.set(key, row);
    }
  }
  return [...map.values()].sort((a, b) => String(b.periodEnd).localeCompare(String(a.periodEnd)));
}

function toLegacyYears(annualRows) {
  return annualRows
    .filter((r) => r.revenue != null || r.netIncome != null)
    .map((r) => ({
      year: r.fiscalYear,
      revenue: r.revenue ?? 0,
      ebitda: r.ebitda ?? r.operatingIncome ?? 0,
      netIncome: r.netIncome ?? 0,
    }));
}

function buildMetrics(latestAnnual, previousAnnual, currency) {
  const metrics = [];
  if (!latestAnnual) return metrics;

  const add = (label, value, format, prev) => {
    if (value == null) return;
    const change =
      prev != null && prev !== 0 ? ((value - prev) / Math.abs(prev)) * 100 : undefined;
    metrics.push({ label, value, format, ...(change !== undefined ? { change } : {}) });
  };

  add("Revenue", latestAnnual.revenue, "currency", previousAnnual?.revenue);
  add("Operating income", latestAnnual.operatingIncome, "currency", previousAnnual?.operatingIncome);
  add("Net income", latestAnnual.netIncome, "currency", previousAnnual?.netIncome);
  if (latestAnnual.grossProfit != null && latestAnnual.revenue) {
    add(
      "Gross margin",
      (latestAnnual.grossProfit / latestAnnual.revenue) * 100,
      "percent",
      previousAnnual?.grossProfit && previousAnnual?.revenue
        ? (previousAnnual.grossProfit / previousAnnual.revenue) * 100
        : undefined
    );
  }

  if (currency) {
    metrics.push({
      label: "Reporting currency",
      value: 0,
      format: "text",
      display: currency.toUpperCase(),
    });
  }

  return metrics;
}

async function fetchReportedFinancials(symbol, freq) {
  if (!finnhub.isEnabled()) return [];
  try {
    const data = await finnhub.finnhubGet("/stock/financials-reported", { symbol, freq });
    return Array.isArray(data?.data) ? data.data : [];
  } catch (err) {
    if (err.status === 403 || err.status === 401) return [];
    throw err;
  }
}

/**
 * @param {object} profile — company profile_json
 * @returns {Promise<object|null>}
 */
async function fetchFinancialsForProfile(profile) {
  if (!profile?.isPublic || !profile?.stock?.ticker) return null;

  const candidates = finnhub.finnhubSymbolCandidates(profile);
  let rawQuarterly = [];
  let rawAnnual = [];
  let resolvedSymbol = null;

  for (const sym of candidates) {
    const [q, a] = await Promise.all([
      fetchReportedFinancials(sym, "quarterly"),
      fetchReportedFinancials(sym, "annual"),
    ]);
    if (q.length || a.length) {
      rawQuarterly = q;
      rawAnnual = a;
      resolvedSymbol = sym;
      break;
    }
  }

  if (!rawQuarterly.length && !rawAnnual.length) return null;

  const quarterly = dedupePeriods(
    rawQuarterly.map((r) => normalizeReportRow(r, "quarterly")).filter((row) => withinYears(row))
  ).slice(0, 24);

  const annual = dedupePeriods(
    rawAnnual
      .filter((r) => r.form === "10-K" || r.quarter == null || r.quarter === 0)
      .map((r) => normalizeReportRow(r, "annual"))
      .filter((row) => withinYears(row))
  ).slice(0, 6);

  const currency =
    profile.stock?.currency ||
    (resolvedSymbol?.endsWith(".OL") ? "NOK" : "USD");

  const latest = annual[0];
  const previous = annual[1];

  return {
    currency,
    source: "finnhub-reported",
    symbol: resolvedSymbol,
    cik: rawAnnual[0]?.cik || rawQuarterly[0]?.cik || null,
    asOf: new Date().toISOString(),
    annual,
    quarterly,
    years: toLegacyYears(annual),
    metrics: buildMetrics(latest, previous, currency),
  };
}

module.exports = {
  fetchFinancialsForProfile,
  parseIncomeStatement,
  normalizeReportRow,
  dedupePeriods,
};
